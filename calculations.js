export function add_two(num){
    return num + 2;
}

export function add(num1, num2){
    return num1 + num2;
}

export function subtract(num3, num4){
    return num3 - num4;
}

export function multiply(num5, num6){
    return num5 * num6;
}

export function divide(num7, num8){
    return num7 / num8;
}